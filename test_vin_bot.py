CACHE_FILE = "cache.json"

def _load_cache() -> dict:
    try:
        with open(CACHE_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {}

def _save_cache(cache: dict) -> None:
    try:
        with open(CACHE_FILE, "w", encoding="utf-8") as f:
            json.dump(cache, f, ensure_ascii=False, indent=2)
    except Exception as e:
        logger.warning("Не удалось сохранить кеш: %s", e)

# Загружаем кеш при старте
_CACHE: dict = _load_cache()

def cache_get(key: str):
    return _CACHE.get(key)

def cache_set(key: str, value, *, allow_err: bool = False) -> None:
    if not allow_err and isinstance(value, dict):
        if value.get("is_error") or value.get("api_status") == "ERR":
            return
    _CACHE[key] = value
    _save_cache(_CACHE)
    
