def _is_vehicle_brand(brand: str) -> bool:
    return brand.upper().strip() in VEHICLE_BRANDS

def _prefix_match_len(article: str, prefixes: list[str]) -> int:
    """Длина самого длинного совпавшего префикса (0 — не совпал ни один)."""
    norm = normalize_article(article)
    best = 0
    for p in prefixes:
        np = normalize_article(p)
        if np and norm.startswith(np) and len(np) > best:
            best = len(np)
    return best

def _suffix_after_dash(article: str) -> str:
    a = article.strip()
    if "-" in a:
        return a.rsplit("-", 1)[1].strip().upper()
    return normalize_article(a)

def _article_sort_key(
    article: str,
    brand: str,
    manu_upper: str,
    prefixes: list[str],
    popularity: dict[str, int],
) -> tuple:
    """
    Ключ сортировки кандидатов. Меньше = лучше.
      1. Своя марка вперёд.
      2. Длиннее совпавший OEM-префикс.
      3. БОЛЬШИЙ числовой суффикс приоритетнее (более новая замена
         артикула: 16400-36240 < 16400-36250).
      4. Цифровой суффикс предпочтительнее буквенного.
      5. Популярность (по всем брендам в ответе).
      6. Длина → стабильность.
    """
    norm = normalize_article(article)
    suffix = _suffix_after_dash(article)
    is_own_brand = brand.upper() != manu_upper
    prefix_len = _prefix_match_len(article, prefixes)
    pop = popularity.get(norm, 1)
    suffix_has_letter = not suffix.isdigit() and any(c.isalpha() for c in suffix)
    suffix_num = int(suffix) if suffix.isdigit() else -1
    return (
        is_own_brand,
        -prefix_len,
        -suffix_num,
        suffix_has_letter,
        -pop,
        len(norm),
        norm,
    )

def pick_primary_oem(
    parts: list[tuple[str, str]],
    cat_id: str,
    manu_name: str,
    shrus_filter: str | None = None,
) -> tuple[tuple[str, str] | None, list[tuple[str, str]]]:
    """
    Универсальный выбор основного OEM. Приоритет:
      1. Артикул марки авто (AUDI для Audi, TOYOTA для Toyota и т.д.)
      2. Артикул OEM-поставщика (BOSCH, MANN, MAHLE и т.п. — не марки авто)
      3. Любой небрендовый артикул
      НИКОГДА — артикул чужой марки авто (MERCEDES для AUDI и т.д.).
    """
    manu_upper = manu_name.upper().strip() if manu_name else ""
    prefixes = (OEM_PREFIXES.get(manu_upper) or {}).get(cat_id, [])

    popularity: dict[str, int] = {}
    for _, a in parts:
        key = normalize_article(a)
        popularity[key] = popularity.get(key, 0) + 1

    # ────────── 1. Артикулы марки авто ──────────
    own_brand_parts = [(b, a) for b, a in parts if b.upper().strip() == manu_upper]

    # ────────── 2. Артикулы OEM-поставщиков (BOSCH, MANN…) ──────────
    supplier_parts = [
        (b, a) for b, a in parts
        if b.upper().strip() in OEM_SUPPLIER_BRANDS
    ]

    # ────────── 3. Прочие НЕ-vehicle бренды ──────────
    other_non_vehicle = [
        (b, a) for b, a in parts
        if not _is_vehicle_brand(b)
        and b.upper().strip() not in OEM_SUPPLIER_BRANDS
    ]

    # Источник кандидатов в порядке приоритета
    if own_brand_parts:
        brand_parts = own_brand_parts
    elif supplier_parts:
        brand_parts = supplier_parts
    elif other_non_vehicle:
        brand_parts = other_non_vehicle
    else:
        # Все остатки — чужие марки авто. Не выбираем как основной OEM.
        return None, []

    # Фильтр по известным OEM-префиксам марки/категории
    if prefixes and own_brand_parts:
        filtered = [
            (b, a) for b, a in brand_parts
            if _prefix_match_len(a, prefixes) > 0
        ]
        if filtered:
            brand_parts = filtered

    # ШРУС позиция
    if shrus_filter and cat_id in SHRUS_POSITION_FILTER:
        pos_filtered = [
            (b, a) for b, a in brand_parts
            if classify_shrus(a, cat_id) == shrus_filter
        ]
        if pos_filtered:
            brand_parts = pos_filtered

    # Приоритет «современный формат»: с дефисом > с пробелом > остальное
    with_dash  = [(b, a) for b, a in brand_parts if "-" in a]
    with_space = [(b, a) for b, a in brand_parts if "-" not in a and " " in a.strip()]
    modern = with_dash if with_dash else (with_space if with_space else brand_parts)

    pool = sorted(
        modern,
        key=lambda x: _article_sort_key(x[1], x[0], manu_upper, prefixes, popularity),
    )

    if not pool:
        return None, []

    primary = pool[0]
    norm_primary = normalize_article(primary[1])
    other_same_brand = [
        (b, a) for b, a in brand_parts if normalize_article(a) != norm_primary
    ]
    return primary, other_same_brand

