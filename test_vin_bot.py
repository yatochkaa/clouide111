async def build_coverage_report(
    session,
    vin: str,
    cats: list[tuple[str, str]] | None = None,
) -> dict:
    """
    Прогоняет curated список cat-ов и собирает отчёт по покрытию.

    Возвращает:
      {
        "checked":      int,
        "working":      int,
        "empty":        int,
        "errors":       int,
        "percent":      int,
        "level":        "weak" | "partial" | "ok" | "error",
        "per_cat":      [{cat_id, name, count, samples, api_status, semantic_status, status, message}, ...],
        "working_list": [...],
        "empty_list":   [...],
        "error_list":   [...],
      }
    """
    if cats is None:
        cats = CURATED_DEBUG_CATS

    per_cat: list[dict] = []
    working_list: list[dict] = []
    empty_list: list[dict] = []
    error_list: list[dict] = []

    for cat_id, cat_name in cats:
        result = await api_get_parts_by_vin(session, vin, cat_id)

        samples = []
        seen_cov = set()
        for item in result.get("items", []):
            for brand, art in item.get("_parts_parsed", []):
                k = f"{brand.upper()}|{normalize_article(art)}"
                if k not in seen_cov:
                    seen_cov.add(k)
                    samples.append((brand, art))
                if len(samples) >= 2:
                    break
            if len(samples) >= 2:
                break

        entry = {
            "cat_id": cat_id,
            "name": cat_name,
            "count": result.get("total", 0),
            "samples": samples,
            "api_status": result.get("api_status"),
            "semantic_status": result.get("semantic_status"),
            "status": result.get("status"),
            "message": result.get("message"),
        }
        per_cat.append(entry)

        if result.get("semantic_status") == "OK":
            working_list.append(entry)
        elif result.get("semantic_status") == "NO":
            empty_list.append(entry)
        else:
            error_list.append(entry)

    checked = len(per_cat) or 1
    percent = int(round(100 * len(working_list) / checked))

    if working_list:
        if percent < 20:
            level = "weak"
        elif percent < 50:
            level = "partial"
        else:
            level = "ok"
    elif error_list:
        level = "error"
    else:
        level = "weak"

    return {
        "checked": len(per_cat),
        "working": len(working_list),
        "empty": len(empty_list),
        "errors": len(error_list),
        "percent": percent,
        "level": level,
        "per_cat": per_cat,
        "working_list": working_list,
        "empty_list": empty_list,
        "error_list": error_list,
    }


