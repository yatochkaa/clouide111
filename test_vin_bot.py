def classify_shrus(article: str, cat_id: str) -> str | None:
    rules = SHRUS_POSITION_FILTER.get(cat_id)
    if not rules:
        return None
    clean = normalize_article(article)
    for label, prefixes in rules.items():
        for p in prefixes:
            if clean.startswith(p):
                return label
    return None

