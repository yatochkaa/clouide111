def _parse_parts_string(parts_str: str) -> list[tuple[str, str]]:
    result = []
    if not parts_str:
        return result
    for chunk in str(parts_str).split(","):
        chunk = chunk.strip()
        if "|" not in chunk:
            continue
        brand, art = chunk.split("|", 1)
        brand, art = brand.strip(), art.strip()
        if not art or art == "-":
            continue
        if brand.upper() in TRUCK_BRANDS:
            continue
        result.append((brand, art))
    return result


def _extract_items_from_raw(data) -> list[dict]:
    items: list[dict] = []

    if isinstance(data, list):
        for row in data:
            if isinstance(row, dict):
                items.append(row)
        return items

    if not isinstance(data, dict):
        return items

    if isinstance(data.get("items"), list):
        for row in data["items"]:
            if isinstance(row, dict):
                items.append(row)

    if isinstance(data.get("data"), list):
        for row in data["data"]:
            if isinstance(row, dict):
                items.append(row)

    inner = data.get("data")
    if isinstance(inner, dict):
        if isinstance(inner.get("items"), list):
            for row in inner["items"]:
                if isinstance(row, dict):
                    items.append(row)
        if isinstance(inner.get("array"), list):
            for row in inner["array"]:
                if isinstance(row, dict):
                    items.append(row)

    arr = data.get("array")
    if isinstance(arr, list):
        for row in arr:
            if isinstance(row, dict):
                items.append(row)

    return items


def _detect_api_error_in_body(data) -> tuple[bool, str | None, str | None]:
    if not isinstance(data, dict):
        return False, None, None

    error_code = data.get("error_code")
    message = data.get("message") or data.get("error") or data.get("detail")

    if error_code:
        return True, str(error_code), str(message or "")

    if isinstance(message, str):
        m = message.lower()
        if "exceeded the number of requests" in m:
            return True, "RATE_LIMIT", message
        if "unauthorized" in m or "invalid key" in m:
            return True, "AUTH", message

    return False, None, message


def normalize_getparts_response(data, *, http_status=None, fallback_message: str | None = None) -> dict:
    api_error, error_code, body_message = _detect_api_error_in_body(data)

    if data is None:
        return {
            "ok": False,
            "is_error": True,
            "is_empty": False,
            "api_status": "ERR",
            "semantic_status": "ERR",
            "status": http_status or "NO_RESPONSE",
            "error_code": error_code,
            "message": fallback_message or body_message or "no response",
            "total": 0,
            "items": [],
            "raw": data,
        }

    if api_error:
        return {
            "ok": False,
            "is_error": True,
            "is_empty": False,
            "api_status": "ERR",
            "semantic_status": "ERR",
            "status": http_status or "API_ERROR",
            "error_code": error_code,
            "message": body_message or fallback_message or "api error",
            "total": 0,
            "items": [],
            "raw": data,
        }

    items = _extract_items_from_raw(data)

    normalized_items = []
    for row in items:
        if not isinstance(row, dict):
            continue
        parts_raw = row.get("parts")
        parsed_parts = _parse_parts_string(parts_raw) if isinstance(parts_raw, str) else []
        normalized_items.append({
            **row,
            "_parts_parsed": parsed_parts,
        })

    total = 0
    for row in normalized_items:
        if row.get("_parts_parsed"):
            total += len(row["_parts_parsed"])

    if total > 0:
        return {
            "ok": True,
            "is_error": False,
            "is_empty": False,
            "api_status": "OK",
            "semantic_status": "OK",
            "status": http_status or 200,
            "error_code": None,
            "message": body_message,
            "total": total,
            "items": normalized_items,
            "raw": data,
        }

    return {
        "ok": True,
        "is_error": False,
        "is_empty": True,
        "api_status": "OK",
        "semantic_status": "NO",
        "status": http_status or 200,
        "error_code": None,
        "message": body_message,
        "total": 0,
        "items": normalized_items,
        "raw": data,
    }


def _item_text(item: dict) -> str:
    bits = []
    for k in ("name", "group", "description", "comment"):
        v = item.get(k)
        if v:
            bits.append(str(v))
    for brand, art in item.get("_parts_parsed", []):
        bits.append(f"{brand} {art}")
    return " ".join(bits).lower()


def filter_items_by_query_intent(items: list[dict], part_text: str, cat_id: str) -> tuple[list[dict], str]:
    if not items:
        return items, "NO_ITEMS"

    q = (part_text or "").lower().strip()
    if not q:
        return items, "NO_QUERY"

    text = " ".join(q.split())

    front_tokens = {"перед", "передний", "передняя", "передние", "front"}
    rear_tokens = {"зад", "задний", "задняя", "задние", "rear"}

    wants_front = any(t in text for t in front_tokens)
    wants_rear = any(t in text for t in rear_tokens)

    out = []
    for item in items:
        hay = _item_text(item)
        if wants_front and ("rear" in hay or "зад" in hay):
            continue
        if wants_rear and ("front" in hay or "перед" in hay):
            continue
        out.append(item)

    if out:
        return out, "FILTER_OK"
    return items, "FILTERED_OUT"

