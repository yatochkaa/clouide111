def parse_parts_string(parts_str: str) -> list[tuple[str, str]]:
    result = []
    for chunk in parts_str.split(","):
        chunk = chunk.strip()
        if "|" not in chunk:
            continue
        brand, art = chunk.split("|", 1)
        brand, art = brand.strip(), art.strip()
        if not art or art == "-":
            continue
        if brand.upper() in TRUCK_BRANDS:
            continue
        result.append((brand, art))
    return result

