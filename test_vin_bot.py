def _extract_parts_items(payload) -> list[dict]:
    """
    Нормализует ответ getPartsbyVIN к списку словарей.
    API может вернуть dict или list[dict].
    """
    if payload is None:
        return []

    if isinstance(payload, list):
        return [x for x in payload if isinstance(x, dict)]

    if isinstance(payload, dict):
        return [payload]

    return []


def _count_parts_items(items: list[dict]) -> int:
    total = 0
    for row in items:
        parts_str = row.get("parts")
        if not parts_str:
            continue
        total += len(parse_parts_string(parts_str))
    return total


def _classify_partsapi_payload(payload, *, http_status=None, cat: str | None = None) -> dict:
    """
    Разделяет три состояния:
    OK  - есть реальные данные
    NO  - ответ валиден, но данных нет
    ERR - ошибка / timeout / rate-limit / bad payload
    """
    if payload is None:
        return _build_partsapi_result(
            ok=False,
            is_error=True,
            is_empty=False,
            status="NO_RESPONSE",
            http_status=http_status,
            message="Пустой ответ или ошибка соединения",
            total=0,
            items=[],
            raw=payload,
            cat=cat,
        )

    if http_status in (401, 429) or (http_status is not None and int(http_status) >= 500):
        message = None
        error_code = None
        if isinstance(payload, dict):
            message = payload.get("message")
            error_code = payload.get("error_code")
        return _build_partsapi_result(
            ok=False,
            is_error=True,
            is_empty=False,
            status=http_status,
            http_status=http_status,
            error_code=error_code,
            message=message or f"HTTP {http_status}",
            total=0,
            items=[],
            raw=payload,
            cat=cat,
        )

    if isinstance(payload, dict) and payload.get("error_code"):
        error_code = str(payload.get("error_code"))
        message = payload.get("message") or "API error"
        return _build_partsapi_result(
            ok=False,
            is_error=True,
            is_empty=False,
            status=http_status or "API_ERROR",
            http_status=http_status,
            error_code=error_code,
            message=message,
            total=0,
            items=[],
            raw=payload,
            cat=cat,
        )

    items = _extract_parts_items(payload)
    if not items:
        return _build_partsapi_result(
            ok=False,
            is_error=True,
            is_empty=False,
            status=http_status or "BAD_PAYLOAD",
            http_status=http_status,
            message="Неожиданный формат ответа getPartsbyVIN",
            total=0,
            items=[],
            raw=payload,
            cat=cat,
        )

    total = _count_parts_items(items)
    if total == 0:
        return _build_partsapi_result(
            ok=True,
            is_error=False,
            is_empty=True,
            status=http_status or 200,
            http_status=http_status or 200,
            message="Данных по категории не найдено",
            total=0,
            items=items,
            raw=payload,
            cat=cat,
        )

    return _build_partsapi_result(
        ok=True,
        is_error=False,
        is_empty=False,
        status=http_status or 200,
        http_status=http_status or 200,
        message=None,
        total=total,
        items=items,
        raw=payload,
        cat=cat,
    )


async def _request_parts_by_vin_raw(
    session: aiohttp.ClientSession,
    vin: str,
    cat: str,
    timeout: int = 25,
) -> tuple[object, object, str | None]:
    """
    Возвращает (payload, http_status_or_special_status, raw_text).
    ВАЖНО: здесь не схлопываем ошибку в [].
    """
    params = {
        "method": "getPartsbyVIN",
        "key": PARTSAPI_KEY_VIN,
        "vin": vin,
        "type": "oem",
        "cat": cat,
    }

    if API_DELAY > 0:
        await asyncio.sleep(API_DELAY)

    try:
        async with session.get(
            BASE_URL,
            params=params,
            timeout=aiohttp.ClientTimeout(total=timeout),
        ) as r:
            raw_text = await r.text()
            logger.info(
                "[PARTSAPI RAW] vin=%s cat=%s http=%s body=%s",
                vin, cat, r.status, raw_text[:300]
            )

            try:
                payload = json.loads(raw_text)
            except json.JSONDecodeError:
                return (
                    {
                        "error_code": "JSON_ERROR",
                        "message": "Не удалось декодировать JSON",
                        "raw_text": raw_text[:1000],
                    },
                    "JSON_ERROR",
                    raw_text,
                )

            return payload, r.status, raw_text

    except (asyncio.TimeoutError, aiohttp.ServerTimeoutError):
        logger.warning("[PARTSAPI RAW] vin=%s cat=%s status=READ_TIMEOUT", vin, cat)
        return None, "READ_TIMEOUT", None

    except aiohttp.ClientError as e:
        logger.warning(
            "[PARTSAPI RAW] vin=%s cat=%s status=CLIENT_ERROR error=%s",
            vin, cat, e
        )
        return (
            {"error_code": "CLIENT_ERROR", "message": str(e)},
            "CLIENT_ERROR",
            None,
        )

    except Exception as e:
        logger.exception(
            "[PARTSAPI RAW] vin=%s cat=%s status=EXCEPTION error=%s",
            vin, cat, e
        )
        return (
            {"error_code": "EXCEPTION", "message": str(e)},
            "EXCEPTION",
            None,
        )


