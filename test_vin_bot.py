def is_position_safe(src_cat: str, dst_cat: str, position: str | None) -> bool:
    """Запрещает переход между категориями одной позиционной пары."""
    if not position:
        return True
    try:
        s, d = int(src_cat), int(dst_cat)
    except (ValueError, TypeError):
        return True
    for group in POSITION_CAT_GROUPS:
        if s in group and d in group:
            return False
    return True

